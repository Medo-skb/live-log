CREATE TABLE USER_MEDIA (
  MEDIA_ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
  USER_ID NUMBER NOT NULL,
  FILE_URL VARCHAR2(500 BYTE) NOT NULL,
  FILE_NAME VARCHAR2(200 BYTE) NOT NULL,
  ORIGIN_NAME VARCHAR2(200 BYTE),
  FILE_SIZE NUMBER(19, 0) NOT NULL,
  FILE_EXT VARCHAR2(20 BYTE) NOT NULL,
  MEDIA_ROLE VARCHAR2(20 BYTE) NOT NULL,
  CREATED_AT TIMESTAMP(6) DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_USER_MEDIA PRIMARY KEY (MEDIA_ID),
  CONSTRAINT FK_USER_MEDIA_USER FOREIGN KEY (USER_ID)
    REFERENCES USERS (USER_ID) ON DELETE CASCADE,
  CONSTRAINT CK_USER_MEDIA_ROLE CHECK (MEDIA_ROLE IN ('PROFILE', 'BANNER'))
);

CREATE INDEX IDX_USER_MEDIA_USER_ROLE_CREATED
ON USER_MEDIA (USER_ID, MEDIA_ROLE, CREATED_AT DESC);
